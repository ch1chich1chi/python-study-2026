# while 条件表达式:
#     循环体语句1
#     循环体语句2
#     ...
# 注意:while循环是通过条件表达式,来控制是否要进行下一次循环

# while 条件表达式:
#     循环体语句1
#     循环体语句2
#     ...
# else:
#   条件为False.循环正常结束时执行

i = 0
while i < 10:
    print("人生苦短,我用Python~")
    i += 1
else:
    print("循环正常结束")

# 案例:
# 计算1-100之间所有偶数的累加之和
total = 0 # 记录累加之和
i = 1 # 循环开始的数字

while i <= 100:
    if i % 2 ==0: # 偶数
        total += i
    i += 1

print(f"1-100之间的偶数的累加之和:{total}")