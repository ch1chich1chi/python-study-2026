# 案例:完成如下需求
# 1.邮箱格式验证:用户输入一个邮箱,验证邮箱格式是否正确(包含一个@和至少一个.),如果输入正确,输出"邮箱格式正确",否则输出"邮箱格式错误"

# 方式一:
# 1,接收用户输入的邮箱
mail = input("请输入邮箱: ")

# 2.判断邮箱的格式
if mail.count("@") == 1 and mail.count(".") >= 1:
    print("邮箱格式正确")
else:
    print("邮箱格式错误")

# 方式二: in 运算符 ---> 判断子串是否存在字符串中,存在,返回True;否则,返回False
# 1,接收用户输入的邮箱
mail = input("请输入邮箱: ")

# 2.判断邮箱的格式
if mail.count("@") == 1 and "." in mail:
    print("邮箱格式正确")
else:
    print("邮箱格式错误")