# 方式一(双指针法):思路是从两边向中间遍历,基于索引获取两边字符对比,如果两边的字符不相等,则不是回文,否则是回文
# 1. 输入一个字符串, 判断该字符串是否是回文(两边对称) 。  "黄山落叶松叶落山黄"  "上海自来水来自海上"
s = input("请输入一个字符串: ")

# 方式一(双指针法): 思路是从两边向中间遍历, 基于索引获取两边字符对比, 如果两边的字符不相等, 则不是回文, 否则是回文。
left = 0 #  左索引
right =  len(s) - 1 # 右索引
flag = True

while left < right:
    if s[left] != s[right]:
        flag = False
        break
    left += 1
    right -= 1

if flag:
    print(f"'{s}' 是回文")
else:
    print(f"'{s}' 不是回文")


# 方式二:
if s == s[::-1]:
    print(f"'{s}' 是回文")
else:
    print(f"'{s}' 不是回文")

