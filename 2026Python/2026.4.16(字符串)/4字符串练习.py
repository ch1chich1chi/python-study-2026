# 6.输入一个混合了字母和数字的字符串(比如 abc123def456),把里面所有的数字字符提取出来,组成一个新的字符串并输出
# 示例:输入:abc123def456 输出:123456
s = input("请输入一个混合了字母和数字的字符串: ")
new_s = ""
for char in s:
    if char.isdigit(): # isdigit() 方法用于判断字符串是否只包含数字字符,如果是数字字符,返回True;否则,返回False
        new_s += char
print("提取出来的数字字符串为:", new_s)

